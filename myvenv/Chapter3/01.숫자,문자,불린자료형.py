# 주석 (comments)

# - 숫자 자료형
# 1. 정수형 : 소수점이 없는 수
# sep="" 이것은 공백 없이 진행된다.
print(1, 3, 0, -1, sep="")

# 2. 실수형 : 소수점이 있는 수
print(1.1, 3.1, 0, -1.4)

# - 문자열 자료형
# "" or ''
# end='' 붙이면 줄내림 없어짐
print("파이썬 너무 재미있다.", end='')
print('파이썬 정말 재미있자너!!')
print('"개 짖는 소리 좀 안나게 해라"라고 그는 외쳤다.')

# - 불리형 자료형
# True or False
print(True) 
print(False)
